package ch02_DataTypes_WrapperClass;

public class C02_SwapInterviewQuestion {

    public static void main(String[] args) {

        int sayi1 = 20;
        int sayi2 = 34;
        int temp ;

        System.out.println("sayi1 = "+sayi1+" sayi2= "+sayi2);

        temp= sayi1 ;

        sayi1 = sayi2 ;
        sayi2 = temp ;

        System.out.println("sayi1 = "+sayi1+" sayi2= "+sayi2);



    }


}
