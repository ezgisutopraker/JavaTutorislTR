package ch04_JavaOperators.jo02_AssignmentOperators;

public class C01_AtamaOperator {

    public static void main(String[] args) {

        //    =	        x = 8	    x = 8
        //    +=	    x += 3	    x = x + 3
        //    -=	    x -= 3	    x = x - 3
        //    *=	    x *= 3	    x = x * 3
        //    /=	    x /= 3	    x = x / 3
        //    %=	    x %= 3	    x = x % 3


        int yas = 33;
        yas +=5;

        System.out.println("yas = " + yas);

        yas *= 2;

        System.out.println("yas = " + yas);

        yas /= 2;

        System.out.println("yas = " + yas);



    }
}
